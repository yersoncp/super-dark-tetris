import type { NextPage } from 'next'
import Head from 'next/head'
import { useCallback, useEffect, useRef, useState } from 'react'
import { Game } from '../src/class/Game'

const Home: NextPage = () => {

  const canvasRef = useRef<HTMLCanvasElement>(null)
  const [game, setGame] = useState<Game | null>(null)
  const [isPaused, setIsPaused] = useState(true)

  console.warn('>>>> Render page');

  useEffect(() => {
    const game = new Game({ canvas: canvasRef.current as HTMLCanvasElement })
    setGame(game)
    console.log('>>>> Game created');
  }, [])

  const isPausedHandler = useCallback(() => {
    if (game) {
      setIsPaused(!isPaused)
      game.pauseOrResumenGame(isPaused)
    }
  }, [game, isPaused])

  return (
    <div className="layout">
      <Head>
        <title>Tetris</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <div className="control">
        <button type="button" onClick={() => isPausedHandler()}>{ isPaused ? 'Play' : 'Pause' }</button>
      </div>

      <div>
        <canvas ref={canvasRef}></canvas>
      </div>
    </div>
  )
}

export default Home
